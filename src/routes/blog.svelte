<h2>Dev Diary #18 - Electric Vanilla Composter</h2>

<div class="row">
	<div class="col-12">
		<p>
			We noticed that many people are using composters to recycle extra plants from Harvesters, as
			well as saplings and leaves from Woodcutters. Unfortunately, cargo doesn't really work
			directly with composters, as they don't have an inventory. This led to people having to place
			hoppers above and below the composters, which is pretty space-inefficient and slow (and
			hoppers are kind of a lag source).
		</p>
		<p>
			So, we've added an electric machine that does what the vanilla composter does, but faster and,
			at the higher levels, better.
		</p>
	</div>

