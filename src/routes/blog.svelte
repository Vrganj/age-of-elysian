<h2>Dev Diary #17 - Some Polish</h2>

<p>
	This week we did a little tweaking with some animations and visuals. For one, Bonfires now have a
	passive and a teleportation animation to make them stand out more. They also have some new sounds.
</p>

<p>
	Crate animations also have been overhauled. Before, the dropped items would be dropped randomly
	around the chest in a single stack, which didn't look very interesting and was kind of annoying to
	grab if it happened to land behind the crate. Now, items are spit out individually which looks
	quite a bit better, and they are thrown in the player's direction so they are all easy to grab.
</p>

